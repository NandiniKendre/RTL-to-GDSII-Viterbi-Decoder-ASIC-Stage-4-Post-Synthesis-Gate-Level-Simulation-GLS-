`timescale 1ns/10ps

module Tb_viterbi_withpad;

////////////////////////////////////////////////////////////
// CLOCK
////////////////////////////////////////////////////////////

reg clk = 0;
always #5 clk = ~clk;

////////////////////////////////////////////////////////////
// PAD-LEVEL INPUTS
////////////////////////////////////////////////////////////

reg clk_pad;
reg rst_n_pad;
reg start_pad;
reg rx_valid_pad;
reg rx_bit0_pad;
reg rx_bit1_pad;

////////////////////////////////////////////////////////////
// PAD-LEVEL OUTPUTS
////////////////////////////////////////////////////////////

wire ready_pad;
wire out_valid_pad;
wire out_bit_pad;

////////////////////////////////////////////////////////////
// INTERNAL MONITOR SIGNALS
////////////////////////////////////////////////////////////

wire ready     = ready_pad;
wire out_valid = out_valid_pad;
wire out_bit   = out_bit_pad;

////////////////////////////////////////////////////////////
// PARAMETERS
////////////////////////////////////////////////////////////

parameter SEQ_LEN = 256;

////////////////////////////////////////////////////////////
// DUT
////////////////////////////////////////////////////////////

viterbi_k7 dut (
    .clk_pad(clk_pad),
    .rst_n_pad(rst_n_pad),
    .start_pad(start_pad),
    .rx_valid_pad(rx_valid_pad),
    .rx_bit0_pad(rx_bit0_pad),
    .rx_bit1_pad(rx_bit1_pad),

    .ready_pad(ready_pad),
    .out_valid_pad(out_valid_pad),
    .out_bit_pad(out_bit_pad)
);

////////////////////////////////////////////////////////////
// CLOCK CONNECTION
////////////////////////////////////////////////////////////

always @(*) clk_pad = clk;

////////////////////////////////////////////////////////////
// SDF
////////////////////////////////////////////////////////////


////////////////////////////////////////////////////////////
// TEST DATA
////////////////////////////////////////////////////////////

reg [SEQ_LEN-1:0] msg;
reg [5:0] state;
reg [1:0] e;

integer i;

////////////////////////////////////////////////////////////
// CONVOLUTIONAL ENCODER
////////////////////////////////////////////////////////////

function [1:0] enc;

    input bit_in;
    input [5:0] state;

    reg [6:0] shift;

    begin

        shift = {bit_in, state};

        enc[1] =
        shift[6]^shift[5]^shift[4]^shift[3]^shift[0];

        enc[0] =
        shift[6]^shift[4]^shift[3]^shift[1]^shift[0];

    end

endfunction

////////////////////////////////////////////////////////////
// VCD
////////////////////////////////////////////////////////////

initial begin

    $dumpfile("viterbi_gls.vcd");
    $dumpvars(0, Tb_viterbi_withpad);

end

////////////////////////////////////////////////////////////
// OUTPUT FILE
////////////////////////////////////////////////////////////

integer fout;

initial begin
    fout = $fopen("decoded.txt","w");
end

always @(posedge clk) begin

    if(out_valid)
        $fwrite(fout,"%b",out_bit);

end

////////////////////////////////////////////////////////////
// MAIN TEST
////////////////////////////////////////////////////////////

initial begin

    /////////////////////////////////////////
    // INITIALIZE
    /////////////////////////////////////////

    rst_n_pad    = 0;
    start_pad    = 0;
    rx_valid_pad = 0;
    rx_bit0_pad  = 0;
    rx_bit1_pad  = 0;

    /////////////////////////////////////////
    // RESET
    /////////////////////////////////////////

    #100;
    rst_n_pad = 1;

    /////////////////////////////////////////
    // TEST VECTOR
    /////////////////////////////////////////

    msg =
    256'hA5A5_F0F0_1234_5678_ABCD_EF01_1357_9BDF_2468_ACED_DEAD_BEEF_CAFE_BABE_0F0F_FFFF;

    state = 0;

    /////////////////////////////////////////
    // START
    /////////////////////////////////////////

    @(posedge clk);

    start_pad = 1;

    @(posedge clk);

    start_pad = 0;

    /////////////////////////////////////////
    // SEND ENCODED DATA
    /////////////////////////////////////////

    for(i=0;i<SEQ_LEN;i=i+1)
    begin

        e = enc(msg[i], state);

        state = {msg[i], state[5:1]};

        @(posedge clk);

        rx_valid_pad = 1;

        rx_bit0_pad = e[0];
        rx_bit1_pad = e[1];

        @(posedge clk);

        rx_valid_pad = 0;

    end

    /////////////////////////////////////////
    // WAIT FOR TRACEBACK
    /////////////////////////////////////////

    #100000;

    $display("GLS Finished");

    $finish;

end

endmodule
